const puppeteer = require('puppeteer');
const fs = require('fs');
const path = require('path');

async function importWhatsAppWorkflow() {
  const browser = await puppeteer.launch({ 
    headless: false, 
    defaultViewport: null,
    args: ['--no-sandbox', '--disable-setuid-sandbox', '--start-maximized']
  });
  
  try {
    const page = await browser.newPage();
    
    console.log('üåê Accessing N8N instance...');
    await page.goto('https://n8n-production-81b6.up.railway.app', { 
      waitUntil: 'networkidle2',
      timeout: 60000 
    });
    
    // Wait for interface to load
    await page.waitForTimeout(5000);
    
    // Check if login is required
    console.log('üîç Checking for login form...');
    const loginForm = await page.$('input[type="email"], input[type="password"], [data-test-id="login-form"]');
    
    if (loginForm) {
      console.log('üîê Login form detected. Please log in manually...');
      console.log('‚è≥ Waiting 30 seconds for manual login...');
      await page.waitForTimeout(30000);
    }
    
    // Try to navigate to workflows page
    console.log('üìã Navigating to workflows page...');
    
    // Try different approaches to get to workflows
    const workflowsSelectors = [
      '[data-test-id="workflows"]',
      'a[href*="workflows"]',
      'nav a:contains("Workflows")',
      '.menu-item:contains("Workflows")'
    ];
    
    let workflowsFound = false;
    for (const selector of workflowsSelectors) {
      try {
        const element = await page.$(selector);
        if (element) {
          await element.click();
          workflowsFound = true;
          console.log('‚úÖ Clicked workflows menu');
          break;
        }
      } catch (e) {
        // Continue trying other selectors
      }
    }
    
    if (!workflowsFound) {
      // Try direct navigation
      console.log('üîÑ Trying direct navigation...');
      await page.goto('https://n8n-production-81b6.up.railway.app/workflows');
    }
    
    await page.waitForTimeout(3000);
    
    // Look for import workflow button or create new workflow
    console.log('üîç Looking for import or create workflow options...');
    
    const importSelectors = [
      '[data-test-id="import-workflow"]',
      'button:contains("Import")',
      '.import-workflow',
      '[aria-label*="import"]'
    ];
    
    const createSelectors = [
      '[data-test-id="create-workflow"]',
      'button:contains("New")',
      'button:contains("Create")',
      '.create-workflow',
      '+',
      '[data-test-id="new-workflow"]'
    ];
    
    let importButton = null;
    let createButton = null;
    
    // Check for import button first
    for (const selector of importSelectors) {
      try {
        importButton = await page.$(selector);
        if (importButton) {
          console.log('‚úÖ Found import button');
          break;
        }
      } catch (e) {}
    }
    
    // Check for create button
    for (const selector of createSelectors) {
      try {
        createButton = await page.$(selector);
        if (createButton) {
          console.log('‚úÖ Found create button');
          break;
        }
      } catch (e) {}
    }
    
    // Read the workflow JSON
    const workflowPath = '/mnt/d/Users/vilma/fly2any/n8n-workflows/whatsapp-automation-complete.json';
    const workflowContent = fs.readFileSync(workflowPath, 'utf8');
    const workflowData = JSON.parse(workflowContent);
    
    console.log(`üìÑ Loaded workflow: ${workflowData.name}`);
    
    if (importButton) {
      console.log('üîÑ Using import workflow method...');
      await importButton.click();
      await page.waitForTimeout(2000);
      
      // Look for JSON import field
      const jsonInput = await page.$('textarea, .json-input, [data-test-id="json-input"]');
      if (jsonInput) {
        console.log('üìù Pasting workflow JSON...');
        await jsonInput.click();
        await jsonInput.evaluate((el, content) => {
          el.value = content;
          el.dispatchEvent(new Event('input', { bubbles: true }));
        }, workflowContent);
        
        // Click import/save button
        const importConfirm = await page.$('button:contains("Import"), button:contains("Save"), [data-test-id="import-confirm"]');
        if (importConfirm) {
          await importConfirm.click();
          console.log('‚úÖ Workflow import initiated');
        }
      }
      
    } else if (createButton) {
      console.log('üîÑ Using create new workflow method...');
      await createButton.click();
      await page.waitForTimeout(3000);
      
      // Check if we're in the workflow editor
      const workflowEditor = await page.$('.workflow-canvas, .node-view, [data-test-id="workflow-canvas"]');
      
      if (workflowEditor) {
        console.log('üéØ In workflow editor - looking for import option...');
        
        // Try to find workflow menu or settings
        const menuSelectors = [
          '[data-test-id="workflow-menu"]',
          '.workflow-settings',
          'button:contains("...")',
          '[aria-label*="menu"]'
        ];
        
        for (const selector of menuSelectors) {
          try {
            const menuButton = await page.$(selector);
            if (menuButton) {
              await menuButton.click();
              await page.waitForTimeout(1000);
              
              // Look for import option in menu
              const importOption = await page.$('li:contains("Import"), .menu-item:contains("Import")');
              if (importOption) {
                await importOption.click();
                console.log('‚úÖ Found import option in menu');
                break;
              }
            }
          } catch (e) {}
        }
        
        // Alternative: Try keyboard shortcut for import (Ctrl+I)
        console.log('üîÑ Trying keyboard shortcut for import...');
        await page.keyboard.down('Control');
        await page.keyboard.press('i');
        await page.keyboard.up('Control');
        await page.waitForTimeout(1000);
        
        // Look for import dialog or JSON input
        const importDialog = await page.$('.import-dialog, .json-import, textarea');
        if (importDialog) {
          console.log('üìù Found import dialog - pasting workflow...');
          await importDialog.click();
          await importDialog.evaluate((el, content) => {
            el.value = content;
            el.dispatchEvent(new Event('input', { bubbles: true }));
          }, workflowContent);
          
          // Click import button
          const confirmButton = await page.$('button:contains("Import"), button:contains("Confirm")');
          if (confirmButton) {
            await confirmButton.click();
            console.log('‚úÖ Workflow imported successfully');
          }
        }
      }
    }
    
    // Wait a bit to see the result
    await page.waitForTimeout(5000);
    
    // Try to activate the workflow
    console.log('üîÑ Looking for workflow activation...');
    const activateSelectors = [
      '[data-test-id="workflow-activate"]',
      'button:contains("Active")',
      '.workflow-active-toggle',
      'input[type="checkbox"][aria-label*="active"]'
    ];
    
    for (const selector of activateSelectors) {
      try {
        const activateButton = await page.$(selector);
        if (activateButton) {
          const isActive = await activateButton.evaluate(el => {
            return el.checked || el.classList.contains('active') || el.getAttribute('aria-pressed') === 'true';
          });
          
          if (!isActive) {
            await activateButton.click();
            console.log('‚úÖ Workflow activated');
          } else {
            console.log('‚úÖ Workflow already active');
          }
          break;
        }
      } catch (e) {}
    }
    
    // Save workflow
    console.log('üíæ Saving workflow...');
    await page.keyboard.down('Control');
    await page.keyboard.press('s');
    await page.keyboard.up('Control');
    await page.waitForTimeout(2000);
    
    // Take screenshot for verification
    await page.screenshot({ 
      path: '/mnt/d/Users/vilma/fly2any/whatsapp-workflow-imported.png', 
      fullPage: true 
    });
    console.log('üì∏ Screenshot saved as whatsapp-workflow-imported.png');
    
    // Test the webhook endpoint
    console.log('üß™ Testing webhook endpoint...');
    
    const testMessage = {
      event: 'whatsapp_message_received',
      data: {
        from: '+1234567890',
        text: 'Ol√°! Preciso de uma cota√ß√£o para voos Miami-S√£o Paulo',
        contactName: 'Test User',
        timestamp: new Date().toISOString(),
        messageId: 'test_msg_123',
        isNewConversation: true
      }
    };
    
    try {
      // Open new tab for webhook test
      const newPage = await browser.newPage();
      await newPage.goto('about:blank');
      
      const testResult = await newPage.evaluate(async (testData) => {
        try {
          const response = await fetch('https://n8n-production-81b6.up.railway.app/webhook/whatsapp', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify(testData)
          });
          return {
            status: response.status,
            ok: response.ok,
            statusText: response.statusText
          };
        } catch (error) {
          return { error: error.message };
        }
      }, testMessage);
      
      console.log('üß™ Webhook test result:', testResult);
      
      if (testResult.ok) {
        console.log('‚úÖ WhatsApp webhook is working!');
      } else if (testResult.status === 404) {
        console.log('‚ö†Ô∏è Webhook not found - may need manual activation');
      } else {
        console.log('‚ö†Ô∏è Webhook test returned:', testResult);
      }
      
      await newPage.close();
      
    } catch (error) {
      console.log('‚ö†Ô∏è Could not test webhook:', error.message);
    }
    
    console.log('\nüéâ WhatsApp Workflow Import Process Complete!');
    console.log('\nüìã Workflow Features:');
    console.log('‚úÖ Webhook endpoint: /webhook/whatsapp');
    console.log('‚úÖ Message processing with intent detection');
    console.log('‚úÖ Lead creation event handling');
    console.log('‚úÖ Auto-response generation');
    console.log('‚úÖ Support ticket creation');
    console.log('‚úÖ Email notifications');
    console.log('\nüîó Webhook URL: https://n8n-production-81b6.up.railway.app/webhook/whatsapp');
    
    console.log('\n‚è≥ Browser will remain open for manual verification...');
    console.log('Press Ctrl+C when finished');
    
    // Keep browser open for manual inspection
    await new Promise(() => {});
    
  } catch (error) {
    console.error('‚ùå Error during workflow import:', error.message);
    
    // Take error screenshot
    try {
      const page = await browser.newPage();
      await page.screenshot({ 
        path: '/mnt/d/Users/vilma/fly2any/import-error.png', 
        fullPage: true 
      });
      console.log('üì∏ Error screenshot saved as import-error.png');
    } catch (e) {}
    
  } finally {
    // Don't close browser automatically to allow manual inspection
    console.log('üîç Browser kept open for inspection. Close manually when done.');
  }
}

// Check if workflow file exists before running
const workflowPath = '/mnt/d/Users/vilma/fly2any/n8n-workflows/whatsapp-automation-complete.json';
if (!fs.existsSync(workflowPath)) {
  console.error('‚ùå Workflow file not found:', workflowPath);
  process.exit(1);
}

console.log('üöÄ Starting WhatsApp Workflow Import Process...');
console.log('üìÅ Workflow file:', workflowPath);
console.log('üåê N8N Instance: https://n8n-production-81b6.up.railway.app');
console.log('üéØ Target webhook: /webhook/whatsapp');

importWhatsAppWorkflow();