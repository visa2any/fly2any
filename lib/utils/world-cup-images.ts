/**
 * Get random hero image for variety
 */
export function getRandomHeroImage(): string {
  const images = Object.values(HERO_IMAGES);
  return images[Math.floor(Math.random() * images.length)];
}

/**
 * Get random fan celebration image
 */
export function getRandomFanImage(): string {
  return FAN_CULTURE_IMAGES.celebration[
    Math.floor(Math.random() * FAN_CULTURE_IMAGES.celebration.length)
  ];
}

/**
 * Get team image by slug
 */
export function getTeamImage(teamSlug: string, type: 'flag' | 'fans' | 'celebration' = 'fans'): string {
  const team = TEAM_IMAGES[teamSlug as keyof typeof TEAM_IMAGES];
  if (!team) {
    return getRandomFanImage();
  }
  return team[type];
}

/**
 * Get stadium image by slug
 */
export function getStadiumImage(stadiumSlug: string, type: 'hero' | 'interior' | 'exterior' = 'hero'): string {
  const stadium = STADIUM_IMAGES_HD[stadiumSlug as keyof typeof STADIUM_IMAGES_HD];
  if (!stadium) {
    return HERO_IMAGES.worldCupStadium;
  }
  return stadium[type];
}

/**
 * Get host country image
 */
export function getHostCountryImage(country: 'usa' | 'canada' | 'mexico', type: 'skyline' | 'culture' | 'landmarks' | 'flag' = 'skyline'): string {
  return HOST_COUNTRY_IMAGES[country][type];
}

/**
 * Get World Cup atmosphere image (for backgrounds)
 */
export function getWorldCupAtmosphere(width = 1920, height = 1080): string {
  const keys = Object.keys(HERO_IMAGES);
  const randomKey = keys[Math.floor(Math.random() * keys.length)];
  return HERO_IMAGES[randomKey as keyof typeof HERO_IMAGES];
}

/**
 * Preload critical images for performance
 */
export function preloadCriticalImages() {
  if (typeof window === 'undefined') return;

  const criticalImages = [
    HERO_IMAGES.worldCupStadium,
    HERO_IMAGES.soccerFans,
    HERO_IMAGES.worldCupTrophy,
  ];

  criticalImages.forEach((src) => {
    const link = document.createElement('link');
    link.rel = 'preload';
    link.as = 'image';
    link.href = src;
    document.head.appendChild(link);
  });
}
