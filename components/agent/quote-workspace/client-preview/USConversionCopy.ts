// ============================================
// HELPER FUNCTIONS
// ============================================

export function getWhySelected(type: string, seed: number = 0): string {
  const reasons = decisionConfidence.whySelected[type as keyof typeof decisionConfidence.whySelected] || [];
  return reasons[seed % reasons.length] || "Carefully selected for your trip.";
}

export function getValuePerDay(total: number, days: number): string {
  const perDay = Math.ceil(total / days);
  return pricePsychology.valueFraming.perDay.replace("{amount}", `$${perDay}`);
}

export function getNormalizingCopy(type: "popular" | "recommended" | "trending" | "value" | "premium"): string {
  return decisionConfidence.normalizing[type];
}

export function getCtaCopy(context: "review" | "confirm" | "book" | "proceed" | "ready"): string {
  return ctaOptimization.primary[context];
}

export function getFinalReassurance(variant: "short" | "medium" | "full"): string {
  return finalReassurance.weGotYou[variant];
}
